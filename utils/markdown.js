const map = {
  "*": "\\*",
  "#": "\\#",
  "(": "\\(",
  ")": "\\)",
  "[": "\\[",
  "]": "\\]",
  _: "\\_",
  "\\": "\\\\",
  "+": "\\+",
  "-": "\\-",
  "`": "\\`",
  "<": "&lt;",
  ">": "&gt;",
  "&": "&amp;",
};
export const md = (string) =>
  string.replace(/[\*\(\)\[\]\+\-\\_`#<>]/g, (m) => map[m]);
